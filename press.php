						<h3>Pressetext</h3>
						<p>
							Stilistisch sagen wir, ist Vedrfölnir (so wird der Habicht in der Nordischen Mythologie genannt) nicht eine gewöhnliche Band, die sich auf Musikgenres beschränkt. Nein, sie bezieht auch mehrere Klassische Elemte mit ein, wie zum Beispiel Klassik, Folklore usw.... Die Experimentierfreudigkeit der Musiker kennt hier keine Grenzen.<br>
							Ihr dürft euch also auf facettenreiche Musik freuen! Thematisch handeln die Songtexte von den Sagas der Nordischen und Germanischen Mythologie und auch eigenen Literarischen Werken, die darauf basieren. Ebenso begeben wir uns thematisch auch außerhalb zu unseren Freunden, Wie den Slawen, den Ägyptern, den Sumerern und Kelten.<br>
							Getreu dem Motto ¨Eine Reise beginnt”, reist und besucht (der) Vedrfölnir also nicht nur die bekannte Nordische Mythologie, sondern ebenso auch andere Mythologien, Themen und musikalische Stilrichtungen. Die Vocals sind sehr warm, peitschend, krächzend und ”hasserfüllt”, wie das Screamen und Growlen. Vereinzeld gibt es Cleane Passagen, in den mehrstimmig gesungen wird.<br>
							Die Gitarren werden sehr schnell und melodisch gespielt. Besonders auffallend ist das Hamronisieren durch Apreggions und anderen Läufen, die man vielleicht von der Band Windir kennt.
						</p>

