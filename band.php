						<h3>Biografie</h3>
						<p>
2010 von drei ambitionierten und hochmotivierten Musikern gegründet, hat sich Vedrfölnir in kurzer Zeit zu einem ernsten Metal-Projekt entwickelt.<br>
Die ersten Proben Mitte des Jahres 2011 waren recht vielversprechend und nach viel zu langer Wartezeit auf einen Proberaum und das Komplettieren der Band, war das Schwerste geschafft und seit ein paar Monaten wurde in Vollbesetzung geprobt was das Zeug hält.<br>
Die ersten Songs waren recht schnell fertig und viele viele weitere stehen in den Startlöchern vollendet und eingeprobt zu werden.<br>
Mit dem Battle in de Räbe 2012 in Haltingen war es dann soweit und Vedrfölnir hatte seinen ersten Auftritt. Bei diesem Bandcontest konnte sich Vedrfölnir sogar durchsetzen und somit seinen ersten Erfolg feiern und zugleich das Metal in de Räbe 2012 eröffnen. Anschließend folgte eine Tour quer durch Süddeutschland mit einem Abstecher in Trier, der sogenannten Black Death Tour 2012.<br>
Weitere Auftritte folgten und auch das Jahr 2013 sollte nicht weniger erfolgreich starten. So nahm Vedrfölnir am Baden in Blut Blood Battle Bandcontest teil und konnte mit einem erfolgreichen zweiten Platz überzeugen. Nachdem sie Ende des Jahres 2013 ihren Proberaum verloren, gab es eine unfreiwillige Auszeit, so war es im Jahr 2014 recht ruhig um Vedrfölnir. Mit einem neuen Proberaum in Kollnau konnte es dann jedoch wieder produktiv weitergehen und so soll im dritten Quartal des Jahres 2015 ihr erster Silberling, ihre EP "Loki's Rache" erscheinen.
 
